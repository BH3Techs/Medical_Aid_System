    @Test
    @Transactional
    void putExistingDocument() throws Exception {
        // Initialize the database
        documentRepository.saveAndFlush(document);

        int databaseSizeBeforeUpdate = documentRepository.findAll().size();

        // Update the document
        Document updatedDocument = documentRepository.findById(document.getId()).get();
        // Disconnect from session so that the updates on updatedDocument are not directly saved in db
        em.detach(updatedDocument);
        updatedDocument
            .format(UPDATED_FORMAT)
            .name(UPDATED_NAME)
            .ownerType(UPDATED_OWNER_TYPE)
            .dateCreated(UPDATED_DATE_CREATED)
            .validity(UPDATED_VALIDITY);

        restDocumentMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedDocument.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedDocument))
            )
            .andExpect(status().isOk());

        // Validate the Document in the database
        List<Document> documentList = documentRepository.findAll();
        assertThat(documentList).hasSize(databaseSizeBeforeUpdate);
        Document testDocument = documentList.get(documentList.size() - 1);
        assertThat(testDocument.getFormat()).isEqualTo(UPDATED_FORMAT);
        assertThat(testDocument.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testDocument.getOwnerType()).isEqualTo(UPDATED_OWNER_TYPE);
        assertThat(testDocument.getDateCreated()).isEqualTo(UPDATED_DATE_CREATED);
        assertThat(testDocument.getValidity()).isEqualTo(UPDATED_VALIDITY);
    }

