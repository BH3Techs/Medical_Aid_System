    private static final String DEFAULT_FORMAT = "AAAAAAAAAA";
    private static final String UPDATED_FORMAT = "BBBBBBBBBB";

    private static final String DEFAULT_NAME = "AAAAAAAAAA";
    private static final String UPDATED_NAME = "BBBBBBBBBB";

    private static final OwnerType DEFAULT_OWNER_TYPE = OwnerType.SPONSOR_DOCUMENT;
    private static final OwnerType UPDATED_OWNER_TYPE = OwnerType.POLICY_DOCUMENT;

    private static final Instant DEFAULT_DATE_CREATED = Instant.ofEpochMilli(0L);
    private static final Instant UPDATED_DATE_CREATED = Instant.now().truncatedTo(ChronoUnit.MILLIS);

    private static final Boolean DEFAULT_VALIDITY = false;
    private static final Boolean UPDATED_VALIDITY = true;

    private static final String ENTITY_API_URL = "/api/documents";
    private static final String ENTITY_API_URL_ID = ENTITY_API_URL + "/{id}";

    private static Random random = new Random();
    private static AtomicLong count = new AtomicLong(random.nextInt() + (2 * Integer.MAX_VALUE));

    @Autowired
    private DocumentRepository documentRepository;

    @Autowired
    private EntityManager em;

    @Autowired
    private MockMvc restDocumentMockMvc;

    private Document document;

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Document createEntity(EntityManager em) {
        Document document = new Document()
            .format(DEFAULT_FORMAT)
            .name(DEFAULT_NAME)
            .ownerType(DEFAULT_OWNER_TYPE)
            .dateCreated(DEFAULT_DATE_CREATED)
            .validity(DEFAULT_VALIDITY);
        return document;
    }

    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Document createUpdatedEntity(EntityManager em) {
        Document document = new Document()
            .format(UPDATED_FORMAT)
            .name(UPDATED_NAME)
            .ownerType(UPDATED_OWNER_TYPE)
            .dateCreated(UPDATED_DATE_CREATED)
            .validity(UPDATED_VALIDITY);
        return document;
    }

    @BeforeEach
    public void initTest() {
        document = createEntity(em);
    }

    @Test
    @Transactional
    void createDocument() throws Exception {
        int databaseSizeBeforeCreate = documentRepository.findAll().size();
        // Create the Document
        restDocumentMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(document)))
            .andExpect(status().isCreated());

        // Validate the Document in the database
        List<Document> documentList = documentRepository.findAll();
        assertThat(documentList).hasSize(databaseSizeBeforeCreate + 1);
        Document testDocument = documentList.get(documentList.size() - 1);
        assertThat(testDocument.getFormat()).isEqualTo(DEFAULT_FORMAT);
        assertThat(testDocument.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testDocument.getOwnerType()).isEqualTo(DEFAULT_OWNER_TYPE);
        assertThat(testDocument.getDateCreated()).isEqualTo(DEFAULT_DATE_CREATED);
        assertThat(testDocument.getValidity()).isEqualTo(DEFAULT_VALIDITY);
    }

